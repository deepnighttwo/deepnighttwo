package com.snda.mzang.tvtogether.server.exception;

public class InvalidatedServerDataException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public InvalidatedServerDataException() {
		super();
	}

	public InvalidatedServerDataException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public InvalidatedServerDataException(String arg0) {
		super(arg0);
	}

	public InvalidatedServerDataException(Throwable arg0) {
		super(arg0);
	}

}
